var side = 15;
var grassArr = [];
var kapuytArr = [];
var karmirArr = [];
var grassEaterArr = [];
var PredatorArr = [];




function setup() {
    frameRate(5);
    createCanvas(matrix[0].length * side, matrix.length * side);
    background('#acacac');
    for (var y = 0; y < matrix.length; ++y) {
        for (var x = 0; x < matrix[y].length; ++x) {
            if (matrix[y][x] == 1) {
                var gr = new Grass(x, y, 1);
                grassArr.push(gr);
            }
            else if (matrix[y][x] == 1) {

            }
            else if (matrix[y][x] == 8) {

            }
            else if (matrix[y][x] == 2) {
                var gr = new GrassEater(x, y, 2);
                grassEaterArr.push(gr);
            }
            else if (matrix[y][x] == 3) {
                var gr = new Predator(x, y, 3);
                PredatorArr.push(gr);
            }
            else if (matrix[y][x] == 4) {
                var gr = new Kapuyt(x, y, 4);
                kapuytArr.push(gr);
            }
            else if (matrix[y][x] == 5) {
                var gr = new Karmir(x, y, 5);
                karmirArr.push(gr);
            }

        }
    }




}

function draw() {

    for (var y = 0; y < matrix.length; y++) {

        for (var x = 0; x < matrix[y].length; x++) {

            if (matrix[y][x] == 1) {
                fill("green");
            }
            else if (matrix[y][x] == 0) {
                fill("#acacac");
            }
            else if (matrix[y][x] == 2) {
                fill(255, 255, 0);
            }
            else if (matrix[y][x] == 3) {
                fill(0, 0, 0);
            }
            else if (matrix[y][x] == 4) {
                fill("blue");
            }
            else if (matrix[y][x] == 5) {
                fill("red");
            }

            rect(x * side, y * side, side, side);



        }
    }


    for (var i in grassArr) {
        grassArr[i].mult()
    }
    for (var i in grassEaterArr) {

        grassEaterArr[i].eat()
        grassEaterArr[i].move()
        grassEaterArr[i].mult()
        grassEaterArr[i].die()
    }
    for (var i in PredatorArr) {
        PredatorArr[i].eat()
        PredatorArr[i].move()
        PredatorArr[i].mult()
        PredatorArr[i].die()
    }
    for (var i in kapuytArr) {
        kapuytArr[i].eat()
        kapuytArr[i].move()
        kapuytArr[i].mult()
        kapuytArr[i].die()
    }
    for (var i in karmirArr) {
        karmirArr[i].eat()
        karmirArr[i].move()
        karmirArr[i].mult()
    }
    if (grassEaterArr.length == 0) {
        for (var i = 0; i < 20; i++) {
            var x = floor(random(matrix[0].length - 1))
            var y = floor(random(matrix.length - 1))
            matrix[y][x] = 2
            var grE = new GrassEater(x, y, 2)
            grassEaterArr.push(grE)

        }
    }
    if (PredatorArr.length == 0) {
        for (var i = 0; i < 20; i++) {
            var x = floor(random(matrix[0].length - 1))
            var y = floor(random(matrix.length - 1))
            matrix[y][x] = 2
            var grE = new Predator(x, y, 2)
            PredatorArr.push(grE)
        }
    }
}

